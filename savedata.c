// SHA1 hash salt retrieved from game.
static const unsigned char salt[] = "VQ(DOdIO9?X3!2GmW#XF";

unsigned int key1;
unsigned int key2;

void savedata_init_key(unsigned int seed) {
    // Set default values the the key if it's zero.
    key1 = seed >> 16;
    if (key1 == 0) {
        key1 = 0xdfa3;
    }
    key2 = seed & 0xffff;
    if (key2 == 0) {
        key2 = 0x215f;
    }
    return;
}

unsigned int savedata_next_key() {
    // This function works but it's a mess. I copied the assembly
    // exactly and then tried simplifying it but it's a confusing
    // function...
    unsigned int a2 = key2 * 0x215f;
    unsigned int a3 = key1 * 0xdfa3;
    unsigned int v1 = ((unsigned long long) a2 * 0x200e263f) >> 45;
    unsigned int t1 = ((unsigned long long) a3 * 0x80088091) >> 32;
    unsigned long long hilo = ((unsigned long long) t1 << 32) + a2;
    t1 >>= 15;
    hilo -= v1 * 0xff8f;
    a2 = t1 << 4;
    unsigned int v0 = t1 << 16;
    v0 -= a2;
    v0 -= t1;
    key2 = hilo;
    key1 = a3 - v0;
    return (key1 << 16) + key2;
}

int savedata_decrypt(unsigned char *data, unsigned int size) {
    // Check that there is at least enough data for a footer.
    if (size < 24)
        return -1;
    // Decrypt the key in the footer.
    data[size-4] = decryption_table[decryption_table[data[size-4]]];
    data[size-3] = decryption_table[decryption_table[data[size-3]]];
    data[size-2] = decryption_table[decryption_table[data[size-2]]];
    data[size-1] = decryption_table[decryption_table[data[size-1]]];
    // Initialize the key.
    savedata_init_key(*(unsigned int *)(data + size - 4));
    unsigned int pos;
    for (pos = 0; pos < (size - 4); pos += 4) {
        data[pos] = decryption_table[data[pos]];
        data[pos+1] = decryption_table[data[pos+1]];
        data[pos+2] = decryption_table[data[pos+2]];
        data[pos+3] = decryption_table[data[pos+3]];
        *(unsigned int*)(data + pos) ^= savedata_next_key();
        data[pos] = decryption_table[data[pos]];
        data[pos+1] = decryption_table[data[pos+1]];
        data[pos+2] = decryption_table[data[pos+2]];
        data[pos+3] = decryption_table[data[pos+3]];
    }
    // Hash the data, excluding the footer and including the salt.
    char md[20];
    SHA_CTX c;
    SHA1_Init(&c);
    SHA1_Update(&c, data, size - 36);
    SHA1_Update(&c, salt, 20);
    SHA1_Final(md, &c);
    // Check that the hash matches the one in the footer.
    return memcmp(md, data + size - 24, 20);
}

int savedata_encrypt(unsigned char *data, unsigned int size) {
    // Check that there is at least enough room for the footer.
    if (size < 24)
        return -1;
    // Create a new key and copy it to the footer.
    srand(time(NULL));
    *(unsigned int *)(data + size - 4) = rand();
    data[size-4] = encryption_table[encryption_table[data[size-4]]];
    data[size-3] = encryption_table[encryption_table[data[size-3]]];
    data[size-2] = encryption_table[encryption_table[data[size-2]]];
    data[size-1] = encryption_table[encryption_table[data[size-1]]];
    // Initialize the key.
    savedata_init_key(*(unsigned int *)(data + size - 4));
    // Hash the data, excluding the footer and including the salt.
    SHA_CTX c;
    SHA1_Init(&c);
    SHA1_Update(&c, data, size - 36);
    SHA1_Update(&c, salt, 20);
    // Copy the hash to the footer.
    SHA1_Final(data - 24, &c);
    unsigned int pos;
    for (pos = 0; pos < (size - 4); pos += 4) {
        data[pos] = encryption_table[data[pos]];
        data[pos+1] = encryption_table[data[pos+1]];
        data[pos+2] = encryption_table[data[pos+2]];
        data[pos+3] = encryption_table[data[pos+3]];
        *(unsigned int*)(data + pos) ^= savedata_next_key();
        data[pos] = encryption_table[data[pos]];
        data[pos+1] = encryption_table[data[pos+1]];
        data[pos+2] = encryption_table[data[pos+2]];
        data[pos+3] = encryption_table[data[pos+3]];
    }
    return;
}
